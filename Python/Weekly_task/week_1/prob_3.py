"""
Write a Python program that takes three numbers as input from the user and prints out the sum of those numbers
"""

num1 = int(input("Enter the number 1: "))
num2 = int(input("Enter the number 2: "))
num3 = int(input("Enter the number 3: "))

sums = num1 + num2 + num3
print(f"Sum of {num1} , {num2}, {num3} is {sums}")